	private void Output_Update_Steps(bool checkForNull, string context, IEnumerable<BindPathStep> steps, bool checkPhaseCondition, string phaseParam)
	{
		if (steps.Count() <= 0)
		{
			return;
		}
		PushIndent();
		if (checkForNull)
		{
			PushIndent();
			Write("        If obj IsNot Nothing Then\r\n");
		}
		List<BindPathStep> list = new List<BindPathStep>();
		string text = null;
		foreach (BindPathStep item in from step in steps
			where step.IsIncludedInUpdate
			orderby step.PhaseList
			select step)
		{
			if (checkPhaseCondition)
			{
				string phaseCondition = GetPhaseCondition(item);
				if (phaseCondition != text)
				{
					if (text != null)
					{
						Write("            End If\r\n");
					}
					text = phaseCondition;
					Write("            If ");
					Write(base.ToStringHelper.ToStringWithCulture(phaseCondition));
					Write(" Then\r\n");
				}
				PushIndent();
				Output_UpdateCall(context, item, phaseParam);
				PopIndent();
			}
			else
			{
				if (text != null)
				{
					Write("            End If\r\n");
				}
				text = null;
				Output_UpdateCall(context, item, phaseParam);
			}
			if (item.BindStatus.HasFlag(BindStatus.HasFallbackValue))
			{
				list.Add(item);
			}
		}
		if (text != null)
		{
			Write("            End If\r\n");
		}
		if (checkForNull)
		{
			if (list.Count > 0)
			{
				Write("        Else\r\n");
				foreach (BindPathStep item2 in list)
				{
					if (checkPhaseCondition)
					{
						Write("            If ");
						Write(base.ToStringHelper.ToStringWithCulture(GetPhaseCondition(item2)));
						Write(" Then\r\n                ");
						Write(base.ToStringHelper.ToStringWithCulture(context));
						Write(".UpdateFallback_");
						Write(base.ToStringHelper.ToStringWithCulture(item2.CodeName));
						Write("(");
						Write(base.ToStringHelper.ToStringWithCulture(phaseParam));
						Write(")\r\n            End If\r\n");
					}
					else
					{
						Write("            ");
						Write(base.ToStringHelper.ToStringWithCulture(context));
						Write(".UpdateFallback_");
						Write(base.ToStringHelper.ToStringWithCulture(item2.CodeName));
						Write("(");
						Write(base.ToStringHelper.ToStringWithCulture(phaseParam));
						Write(")\r\n");
					}
				}
			}
			Write("        End If\r\n");
			PopIndent();
		}
		PopIndent();
	}

