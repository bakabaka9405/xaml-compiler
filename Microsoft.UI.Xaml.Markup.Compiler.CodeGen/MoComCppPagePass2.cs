			Write(base.ToStringHelper.ToStringWithCulture(ba.MemberType));
			Write(" value, ::Platform::String^ targetNullValue)\r\n    {\r\n        if (value == nullptr && targetNullValue != nullptr)\r\n        {\r\n");
			if (ba.MemberType.IsString())
			{
				Write("            value = targetNullValue;\r\n");
			}
			else
			{
				Write("            value = ");
				Write(base.ToStringHelper.ToStringWithCulture(ba.MemberType.GetStringToThing("targetNullValue")));
				Write(";\r\n");
			}
			Write("        }\r\n");
		}
		else
		{
			Write("    static void Set_");
			Write(base.ToStringHelper.ToStringWithCulture(ba.MemberDeclaringType.MemberFriendlyName()));
			Write("_");
			Write(base.ToStringHelper.ToStringWithCulture(ba.MemberName));
			Write("(");
			Write(base.ToStringHelper.ToStringWithCulture(ba.MemberTargetType));
			Write(" obj, ");
			Write(base.ToStringHelper.ToStringWithCulture(ba.MemberType));
			Write(" value)\r\n    {\r\n");
		}
		if (ba.IsAttachable)
		{
			Write("        ");
			Write(base.ToStringHelper.ToStringWithCulture(ba.MemberDeclaringType.CppCXName(IncludeHatIfApplicable: false)));
			Write("::Set");
